function convertToFahrenheit(celsius) {
  return (celsius * 9/5) + 32;
}

let celsiusTemp1 = 0;
console.log(celsiusTemp1 + "°C is equal to " + convertToFahrenheit(celsiusTemp1) + "°F");
let celsiusTemp2 = 100;
console.log(celsiusTemp2 + "°C is equal to " + convertToFahrenheit(celsiusTemp2) + "°F");
let celsiusTemp3 = 37;
console.log(celsiusTemp3 + "°C is equal to " + convertToFahrenheit(celsiusTemp3) + "°F");